const str = "This is a pen."
const upperStr = str.toUpperCase()
console.log(upperStr) //=> THIS IS A PEN.

//大文字にしたものを、小文字にしている
const lowerStr = upperStr.toLowerCase()
console.log(lowerStr) //=> this is a pen.

